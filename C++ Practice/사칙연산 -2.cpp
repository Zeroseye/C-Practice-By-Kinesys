#include <iostream>

using namespace std;

int main(void) {
    int one = 1;
    int two = 2;
    int three = 3;
    int four = 4;

    cout << "one + two = "<< one + two << endl;
    cout << "\n four - two = "<< four - two << endl;
    cout << "\n four * three = "<< four * three << endl;
    cout << "\n four / two = "<< four / two << endl;
    cout << "\n four % two = "<< four % two << endl;

    return 0;
}